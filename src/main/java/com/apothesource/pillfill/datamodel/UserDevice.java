package com.apothesource.pillfill.datamodel;

import java.io.Serializable;

public class UserDevice implements Serializable {

    private static final long serialVersionUID = -1L;

    private String deviceId;
    private String type = "com.google.gcm";
    private long creationDate;
    private String name;
    private String uuid;
    private String connectionUri;


    public String getDeviceId() {
        return this.deviceId;
    }

    public void setDeviceId(String deviceId) {
        this.deviceId = deviceId;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String deviceType) {
        this.type = deviceType;
    }

    public String getConnectionUri() {
        return connectionUri;
    }

    public void setConnectionUri(String connectionUri) {
        this.connectionUri = connectionUri;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public long getCreationDate() {
        return creationDate;
    }

    public void setCreationDate(long creationDate) {
        this.creationDate = creationDate;
    }

    public static class UserDeviceWSResponse {

        public boolean success;
        public String error;
        public UserDevice device;

        public UserDeviceWSResponse(boolean success, String error, UserDevice device) {
            this.success = success;
            this.error = error;
            this.device = device;
        }
    }
}
